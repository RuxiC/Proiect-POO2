package com.unibuc.proiect.pao;

public class Editura {
    private String nume;
    private String adresa;

    public Editura(String nume, String adresa) {
        this.nume = nume;
        this.adresa = adresa;
    }

    public String getNume() {
        return nume;
    }

    public String getAdresa() {
        return adresa;
    }
}
